void daedalianOpusPasswordLookup( char *password_in ){
 char password[5];
 password[4]=0; strncpy(password, password_in, 4);
 for( int i=0; i<4; i++ ){
  password[i] &= ~32;
 }
 int i = 0;
 while( *daedalianOpusLevels[i][0] ){
  if( ! strcmp( daedalianOpusLevels[i][0], password ) ){
   printf("Selected 'Daedalian Opus' level No.%d, password '%s'\n", 2+i, password);
   configureBoardFromString( daedalianOpusLevels[i][1] );
   char *piecesEnabled = daedalianOpusLevels[i][2];
   for( int j=0; j<12; j++ ){
    inUse[j] = (piecesEnabled[j]=='0');
   }
   return;
  }
  i++;
 }
 printf("Unrecognised 'Daedalian Opus' password '%s'\n", password);
 exit(1);
}